    private void processButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processButtonActionPerformed

        int selectedRow = workrequestTable.getSelectedRow();

        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) workrequestTable.getValueAt(selectedRow, 0);
            ManageWorkRequestJPanel manageWorkRequestJPanel = new ManageWorkRequestJPanel(userProcessContainer, enterprise, request,userAccount,doctorOrganization,business);
            userProcessContainer.add("manageWorkRequestJPanel", manageWorkRequestJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            

        } else {
            JOptionPane.showMessageDialog(null, "Please Select A Patient To Process","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_processButtonActionPerformed

    private void assignButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignButtonActionPerformed

        int selectedRow = workrequestTable.getSelectedRow();

        
        if (selectedRow >= 0) {
            WorkRequest request = (WorkRequest) workrequestTable.getValueAt(selectedRow, 0);
                request.setReceiver(userAccount);
                request.setStatus("Pending");
                populateTable();
        }else {
            JOptionPane.showMessageDialog(null, "Choose a request to process.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_assignButtonActionPerformed


