    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workrequestTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : doctorOrganization.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[7];
            if(request instanceof DoctorTestWorkRequest){
            row[0] = request;
            row[1] = request.getAge();
            row[2] = request.getSender().getEmployee().getName();
            row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[4] = request.getDate();
            row[5] = request.getTime();
            String message = ((DoctorTestWorkRequest)request).getMessage();
            row[6] = message;

            model.addRow(row);}
        }
    }

