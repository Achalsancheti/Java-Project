    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed

        int dialogButton = JOptionPane.YES_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Are You Sure You Want To Send Message To Your Relative?");
        if(dialogResult==JOptionPane.YES_OPTION){
            String date = new java.text.SimpleDateFormat("MM/dd/yyyy").format(new Date());
            String time = new java.text.SimpleDateFormat("h:mm:ss a").format(new Date());

            DoctorMessageWorkRequest doctorMessageWorkRequest = new DoctorMessageWorkRequest();
            doctorMessageWorkRequest.setMessage("Vital Sign Is In Danger");
            doctorMessageWorkRequest.setStatus("Pending");
            doctorMessageWorkRequest.setDate(date);
            doctorMessageWorkRequest.setTime(time);

            Enterprise ent = null;
        Organization org = null;
        for(Network network : business.getNetworkList()){
            for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprise instanceof HomeEnterprise)
                        ent=enterprise;
            
        
        
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof PatientRelativeOrganization){
                org = organization;
                break;
            }
        }
            }
        }

            if (org!=null){
                org.getWorkQueue().getWorkRequestList().add(doctorMessageWorkRequest);
                userAccount.getWorkQueue().getWorkRequestList().add(doctorMessageWorkRequest);
            }

            JOptionPane.showMessageDialog(null, "Message Has Been Sent To Patient's Relative","Success",JOptionPane.INFORMATION_MESSAGE);
            populateTable();

        }
    }//GEN-LAST:event_sendButtonActionPerformed

