    private void populateCombo(){
        organizationComboBox.removeAllItems();
        for (Organization.Type type : Organization.Type.values()){
            if (((!type.getValue().equals(Organization.Type.Admin.getValue()))&&
                    (!type.getValue().equals(Organization.Type.Patient.getValue()))&&
                    (!type.getValue().equals(Organization.Type.PatientRelative.getValue()))&&
                    (!type.getValue().equals(Organization.Type.Doctor.getValue()))&&
                    (!type.getValue().equals(Organization.Type.Donor.getValue()))&&
                    (!type.getValue().equals(Organization.Type.Nurse.getValue()))&&
                    (!type.getValue().equals(Organization.Type.Receptionist.getValue()))&&
                    (!type.getValue().equals(Organization.Type.VaccineManager.getValue()))))
                
                organizationComboBox.addItem(type);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationTable.getModel();
        model.setRowCount(0);
        
        for (Organization organization : organizationDirectory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }

