    private void addorganizationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addorganizationButtonActionPerformed
        Type type = (Type) organizationComboBox.getSelectedItem();

        if (!organizationDirectory.getOrganizationList().isEmpty()) {
            Boolean organizationFlag = false;
            for (Organization net : organizationDirectory.getOrganizationList()) {
                if (net.getName().equalsIgnoreCase(type.getValue())) {

                    JOptionPane.showMessageDialog(null, " The organization  "
                            + "already exists.", "Warning",
                            JOptionPane.WARNING_MESSAGE);
                    organizationFlag = true;
                    break;
                }

            }
            if (!organizationFlag) {
                Organization o = organizationDirectory.createOrganization(type);
                JOptionPane.showMessageDialog(null, "Organization Has Been Added Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
        }
        } else {

            Organization o = organizationDirectory.createOrganization(type);
            JOptionPane.showMessageDialog(null, "Organization Has Been Added Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
            populateTable();
        }
    }//GEN-LAST:event_addorganizationButtonActionPerformed

